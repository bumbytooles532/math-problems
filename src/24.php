<?php
// This PHP code is used to demonstrate basic arithmetic operations.
// The first operation adds 2 and 3.
$sum = $a + $b;
echo "The sum of $a and $b is: $sum";

// Example usage of the sum function in a loop.
$a = 5; // Initial value
$b = 10; // Another initial value

for ($i = 0; $i < $b; $i++) {
    $result = $a + $i;
    echo "The result is: $result";
}
?>
